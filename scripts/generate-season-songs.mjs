import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { randomUUID } from "node:crypto";
import { parse } from "csv-parse/sync";

const root = resolve(fileURLToPath(new URL("..", import.meta.url)));

function clean(value) {
  return typeof value === "string" ? value.trim() : "";
}

function numberOrZero(value) {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? Math.max(0, parsed) : 0;
}

function parseVoters(value) {
  try {
    const parsed = JSON.parse(value ?? "[]");
    return Array.isArray(parsed)
      ? parsed.filter((voter) => typeof voter === "string")
      : [];
  } catch {
    return [];
  }
}

function toTime(row, season) {
  if (season !== 3) return undefined;

  const hour = Number(row.time_slot);
  const minute = Number(row.time_minute);
  if (!Number.isInteger(hour) || !Number.isInteger(minute) || hour < 0 || hour > 23 || minute < 0 || minute > 59) {
    return undefined;
  }

  return `${String(hour).padStart(2, "0")}:${String(minute).padStart(2, "0")}`;
}

function timeValue(time) {
  if (!time || !/^\d{2}:\d{2}$/.test(time)) return Number.POSITIVE_INFINITY;
  const [hour, minute] = time.split(":").map(Number);
  return hour * 60 + minute;
}

function createdAtValue(createdAt) {
  const value = Date.parse(createdAt ?? "");
  return Number.isNaN(value) ? Number.POSITIVE_INFINITY : value;
}

function orderSongs(songs, season) {
  return [...songs]
    .sort((a, b) => {
      if (season === 3) {
        const byTime = timeValue(a.time) - timeValue(b.time);
        if (byTime !== 0) return byTime;
      } else {
        const byVotes = b.votes - a.votes;
        if (byVotes !== 0) return byVotes;
      }

      return createdAtValue(a.createdAt) - createdAtValue(b.createdAt);
    })
    .map((song, index) => ({ ...song, trackOrder: index + 1 }));
}

async function loadSeason(season) {
  const filename = resolve(root, `songs-season${season}.csv`);
  const source = await readFile(filename, "utf8");
  const rows = parse(source, {
    columns: true,
    bom: true,
    skip_empty_lines: true,
    relax_column_count: true,
  });

  const songs = rows.map((row) => ({
    id: `s${season}-${clean(row.id) || randomUUID()}`,
    season,
    nickname: clean(row.nickname),
    title: clean(row.title) || "제목을 불러오지 못한 곡",
    comment: row.comment ?? "",
    youtubeUrl: clean(row.youtube_url),
    thumbnailUrl: clean(row.thumbnail_url) || undefined,
    votes: numberOrZero(row.votes),
    createdAt: clean(row.created_at) || undefined,
    time: toTime(row, season),
    trackOrder: 0,
    voters: parseVoters(row.voters),
  }));

  return orderSongs(songs, season);
}

const allSeasons = Object.fromEntries(
  await Promise.all([1, 2, 3].map(async (season) => [season, await loadSeason(season)]))
);

const output = `/* This file is generated by scripts/generate-season-songs.mjs. Do not edit manually. */\n\nexport type Season = 1 | 2 | 3;\n\nexport type Song = {\n  id: string;\n  season: Season;\n  nickname: string;\n  title: string;\n  artist?: string;\n  comment: string;\n  youtubeUrl: string;\n  thumbnailUrl?: string;\n  votes: number;\n  createdAt?: string;\n  time?: string;\n  trackOrder: number;\n  voters: string[];\n};\n\nexport const songsBySeason: Record<Season, Song[]> = ${JSON.stringify(allSeasons, null, 2)} as Record<Season, Song[]>;\n`;

await writeFile(resolve(root, "lib/seasonSongs.generated.ts"), output);
